let number = 10;

if (number == 1) {
    console.log("One");
} else if (number == 10) {
    console.log("Ten");
} else if (number == 100) {
    console.log("Hundred");
} else if (number == 1000) {
    console.log("Thousand");
} else if (number == 10000) {
    console.log("Ten thousand");
} else if (number == 100000) {
    console.log("1 Lakh");
} else {
    console.log("You've entered wrong input.");
}   